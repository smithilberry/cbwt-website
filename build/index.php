            <h1 id="activities">Tour Activities</h1>
            <h5>The Camp Sites</h5>
            <p>We usually rent out a few sites depending on the amount of attendees, but each individual camp site comes with a picnic table and a tire rim for burning wood. The grounds themselves provide wood and ice for a small fee as well as showers and restrooms that are cleaned daily. There is ample tree canopies for shade and the sites sit only a moment from the lake</p>
            <h5>Seneca Lake</h5>
            <p>The lake rests at the bottom of the camp sites and gives unfettered access to the 37 mile long lake. Because the lake is so large we dont suggest straying too far from the shore as you may not recognize the camp from far away. There are kayaks to rent for an additional fee, and there are some boat rentals facilities further away that have full-day and half-day rental service</p>
            <h5>Biking</h5>
            <p>While the biking day is saturday, please feel free to ride your bike whenever you please, although the camp is at the bottom of a steep hill (The bikes are brought to the furthest point via vehicle on bike day). The roads are mostly straight with a variety of inclines and declines. The roads are not heavily trafficed but do be safe and aware of your surroundings</p>
            <h5>Wine Tasting (and one brewery)</h5>
            <p>The road on which the camp sites sit have numerous amounts of wineries spaced out along the way. Some are a few miles apart and others just a short ride. During the biking tour we stop at most wineries and partake in wine tasting as a group though sometimes the group is staggered. The price varies but the average is about $4 per tasting which usually comes with about 5-6 wines to taste. Feel free to buy bottles of wine along the way. Some people bring backpacks hold additional bottles along the ride</p>
            <h5>The Garlic Festival</h5>
            <p>The Garlic Festival is the final large activity of the weekend, it is also how the trip is annually scheduled. For us, it begins Sunday afternoon where traditionally those that are leaving for the weekend finally dispatch after having one final celebratory group outing. The festival has free entrance and takes place at the initial winery that we visit on Saturday, Fox Run Vineyards. It houses dozens of vendors, food tents and live music. Of course, if you still want some wine, the winery itself is open that day as well</p>
