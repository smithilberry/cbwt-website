            <h5>Physical Risks</h5>
            <p>The tour takes place at the start of the typically hottest month of the year, so prepare for heat and sweat. The biking tour can be greuling on the body, especially when using a bicycle that is not well fitted for your body, so keep in mind any physical limitations. Heat and physical stress mixed with alcohol consumption can be dangerous, SO PACE YOURSELF. CBWT is not a competition - there is no winner's medal</p>
