            <h1 id="faq">Frequently Asked Questions</h1>
            <h5>What are the camp sites like?</h5>
            <p class="has-sibling">You can find more information about the <a href="//www.backacherscampsites.com/" target="_blank">Back-Achers Campsite</a> at their website. Below you can see an outlined map of the camp area:</p>
            <p><a href="images/map-bac.png" target="_blank"><img id="camp-map" src="images/map-bac.png" alt="Area map of backachers campgrounds" /></a></p>
            <h5>How far are the nearest towns?</h5>
            <p>The closest town to the camp is Penn Yan which is roughly 10 miles North/North-West. Watkins Glen is about 20 miles South</p>
            <h5>What other attractions are in the area?</h5>
            <p>At Watkins Glen is the <a href="//www.theglen.com/?homepage=true" target="_blank">Watkins Glen International</a> where different events are held through the weekend</p>
            <h5>How far is the bike ride?</h5>
            <p class="has-sibling">The biking tour is roughly 12 miles in length, with periodic stops at wineries about every 1.5 miles. Below is an outlinned trail of all the stops:</p>
            <p><iframe id="route-map" src="https://www.google.com/maps/d/embed?mid=1z6M77jfPOUU2Tnx2CMrFUTjhyOo"></iframe></p>
            <h5>How long does the biking take?</h5>
            <p>About 6 hours. The wineries close at 5PM so the tour tries to reach the final winery by 4:30PM</p>
            <h5>Can I exclude myself from activities?</h5>
            <p>You certainly can, but be sure to bring something to entertain yourself for the times where you may be alone</p>
            <h5>Can I bring my pet?</h5>
            <p>It is not recommended that you bring your pet with you for extended hours/overnight camping</p>
            <h5>Can I rent a bike?</h5>
            <p>Yes, though it is suggested you bring your own bicycle, you can find different rental locations <a href="https://www.google.com/search?q=penn+yan+bike+rental" target="_blank">here</a></p>
