            <h1 id="about">Get ready for CBWT <?php echo date(Y);?>!</h1>
            <h5>What is CBWT?</h5>
            <p>CampBikeWineTour, or CBWT, is an annual trip to Himrod, New York where a group of friends (and strangers!) embark on a weekend of camping, biking and wine tasting around the beautiful Seneca Lake</p>
            <h5>When is it?</h5>
            <p>Every year during the first weekend in August. The trip is centered around the Garlic Festival at Fox Run Vineyards, so the dates always change. The trip usually runs from a Thursday to Monday but the important day is Saturday, the wine tasting and biking day</p>
            <h5>Where is it?</h5>
            <p class="map">We travel to the beautiful Seneca Lake in upstate New York. Seneca Lake is part of the Finger Lakes, a series of lakes parallel to each other that form the shape of a hand. The bike tour starts at <a href="http://www.foxrunvineyards.com/" target="_blank">Fox Run Vineyards</a> and ends at <a href="http://mileswinecellars.com/" target="_blank">Miles Wine Cellars</a></p>
          </div>
